const int box_tags_size = 109;

extern int box_html_tag_id(char* string) {

   int l = 0;
   int r = box_tags_size - 1;
   int result;

   while (l <= r) {
        int m = l + (r - l) / 2;

        result = strcmp(string, box_html_tags_string[m]);

        if (result == 0) {
            return m;
        }
        if (result < 0) {
            r = m - 1;
        }
        else {
            l = m + 1;
        }
   }

   return -1;
}



